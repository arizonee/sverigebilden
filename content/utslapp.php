Sen den tidiga industrialiseringen har utsläppen i världen stadigt ökat. Under det man brukar kalla “den andra industriella revolutionen” mellan cirka 1871-1914 exploderade utvecklingen inom svensk industri, och många välkända företag såg dagens ljus. Bland annat LM Ericsson med sina uppfinningar kring telefonen och Bofors tack vare Alfred Nobels sprängämnen. I tiden efter andra världskriget ökade utvecklingen och 1947 låg Sverige över det globala snittet över utsläpp av koldioxid. Det skulle vi göra ända fram till 1977. Då vänder kurvan neråt mot minskade utsläpp och det har den gjort ända sedan dess. Iallafall här i Sverige.
<br>
<br>
Jonas Allerup på Naturvårdsverket berättar att Sverige hade haft en minskning av sina utsläpp under många år och innan 1990 var de framför allt växthusgasutsläppen från oljepannor för uppvärmning av bostadshus som minskade då vi började gå över till andra bränslen och fjärrvärme. Han menar även att oljekrisen på 1970-talet kan vara en förklarande faktor till den drastiska minskningen. Även politiska vindar är en faktor till den svenska minskningen. Idag fortsätter den globala kurvan skarpt uppåt, samtidigt som den svenska utsläppskurvan sakta minskar för varje år. 
<br>
<br>
Men myntet har flera sidor, och Naturvårdsverket skriver något tänkvärt på sin hemsida. 
“Under de senaste tjugo åren har de konsumtionsbaserade utsläppen i Sverige minskat med trettio procent. Under samma period har andelen utsläpp som sker i andra länder, men som orsakats av vår svenska konsumtion ökat med nästan femtio procent.”
