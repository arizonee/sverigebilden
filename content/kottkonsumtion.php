        Sverige älskar kött. Faktum är att hela världen också gör det. Konsumtionen av fisk, skaldjur och nötkött ökar i hela världen, detta på grund utav ökad levnadsstandard och ökade inkomster globalt sett. Sverige har en väldigt tydlig ökning värd att peka ut.<br><br>
        År 1970 åt svensken i genomsnitt cirka 50,7 kilo kött per år. Detta inkluderar all köttkonsumtion. Allt ifrån fryst nötkött och gris till hemslaktat får eller innanmat. 1990 hade den siffran endast stigit till 53,6 kg per person och år. Men när 2000-talet tar vid händer något. Siffrorna stiger och år 2016 har plötsligt konsumtionen ökat till 82,2 kg per person och år.
